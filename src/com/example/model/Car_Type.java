package com.example.model;

public class Car_Type {
	private String Car_Type;
	private String Car_Name;
	private double Car_Worth;
	private double Car_RentMoney;
	private int    Car_Count;
	public String getCar_Type() {
		return Car_Type;
	}
	public void setCar_Type(String car_Type) {
		Car_Type = car_Type;
	}
	public String getCar_Name() {
		return Car_Name;
	}
	public void setCar_Name(String car_Name) {
		Car_Name = car_Name;
	}
	public double getCar_Worth() {
		return Car_Worth;
	}
	public void setCar_Worth(double car_Worth) {
		Car_Worth = car_Worth;
	}
	public double getCar_RentMoney() {
		return Car_RentMoney;
	}
	public void setCar_RentMoney(double car_RentMoney) {
		Car_RentMoney = car_RentMoney;
	}
	public int getCar_Count() {
		return Car_Count;
	}
	public void setCar_Count(int car_Count) {
		Car_Count = car_Count;
	}
	
}
