package com.example.model;

public class Employee {
	private int 	E_Id;
	private String 	E_Name;
	private String  E_Sex;
	private double  E_Salary;
	private String  E_Tel;
	private String  E_Address;
	private String  E_Position;
	private int     E_Age;
	
	public int getE_Id() {
		return E_Id;
	}
	public void setE_Id(int e_Id) {
		E_Id = e_Id;
	}
	public String getE_Name() {
		return E_Name;
	}
	public void setE_Name(String e_Name) {
		E_Name = e_Name;
	}
	public String getE_Sex() {
		return E_Sex;
	}
	public void setE_Sex(String e_Sex) {
		E_Sex = e_Sex;
	}
	public double getE_Salary() {
		return E_Salary;
	}
	public void setE_Salary(double e_Salary) {
		E_Salary = e_Salary;
	}
	public String getE_Tel() {
		return E_Tel;
	}
	public void setE_Tel(String e_Tel) {
		E_Tel = e_Tel;
	}
	public String getE_Address() {
		return E_Address;
	}
	public void setE_Address(String e_Address) {
		E_Address = e_Address;
	}
	public String getE_Position() {
		return E_Position;
	}
	public void setE_Position(String e_Position) {
		E_Position = e_Position;
	}
	public int getE_Age() {
		return E_Age;
	}
	public void setE_Age(int e_Age) {
		E_Age = e_Age;
	}
}
